# Function to get the first element of an array
def get_first_element(array):
    return array[0]

# Example usage
input_array = [3, 7, 1, 9, 4]

# Retrieving the first element of the array
first_element = get_first_element(input_array)

# Printing the result
print("The first element of the array is:", first_element)
